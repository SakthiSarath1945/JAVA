import java.util.*;
 class mutiples27 {
public static void main(String[] args) {
    Scanner sc=new Scanner(System.in);
    System.out.println("Enter the number:");
    int a=sc.nextInt();
    if(a%27==0){
        System.out.println("multiples of 27");
    }
    else{
        System.out.println("not multiples of 27");
    }
}    
}
